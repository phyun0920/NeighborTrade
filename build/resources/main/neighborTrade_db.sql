-- NeighborTrade PostgreSQL schema and sample DML
-- 1차 MVP 기준: 회원, 동네 인증, 판매글, 거래, 후기, 관리자 기본 기능
-- 실행 전 주의:
-- - 이 스크립트는 기존 테이블을 삭제한 뒤 다시 생성합니다.
-- - 비밀번호는 PostgreSQL pgcrypto의 bcrypt 형식 crypt()로 저장합니다.

CREATE EXTENSION IF NOT EXISTS pgcrypto;

DROP TABLE IF EXISTS review CASCADE;
DROP TABLE IF EXISTS trade CASCADE;
DROP TABLE IF EXISTS product_post CASCADE;
DROP TABLE IF EXISTS location_verification CASCADE;
DROP TABLE IF EXISTS member CASCADE;
DROP TABLE IF EXISTS neighborhood CASCADE;
DROP FUNCTION IF EXISTS set_updated_at CASCADE;

CREATE OR REPLACE FUNCTION set_updated_at()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = CURRENT_TIMESTAMP;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

-- =========================
-- neighborhood
-- =========================
CREATE TABLE neighborhood (
    id                   BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    sido                 VARCHAR(30)  NOT NULL,
    sigungu              VARCHAR(30)  NOT NULL,
    emd_name             VARCHAR(50)  NOT NULL,
    display_name         VARCHAR(120) NOT NULL UNIQUE,
    center_latitude      DOUBLE PRECISION NOT NULL,
    center_longitude     DOUBLE PRECISION NOT NULL,
    verify_radius_meters INTEGER NOT NULL
);

CREATE INDEX idx_neighborhood_emd_name ON neighborhood (emd_name);

-- =========================
-- member
-- =========================
CREATE TABLE member (
    id                       BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    username                 VARCHAR(50)  NOT NULL UNIQUE,
    password                 VARCHAR(255) NOT NULL,
    email                    VARCHAR(100) NOT NULL UNIQUE,
    nickname                 VARCHAR(50)  NOT NULL,
    login_type               VARCHAR(20)  NOT NULL DEFAULT 'LOCAL',
    provider                 VARCHAR(30),
    provider_id              VARCHAR(100),
    profile_image_url        VARCHAR(500),
    role                     VARCHAR(30)  NOT NULL DEFAULT 'ROLE_USER',
    local_verified           BOOLEAN      NOT NULL DEFAULT FALSE,
    verified_neighborhood_id BIGINT,
    manner_score             DOUBLE PRECISION NOT NULL DEFAULT 36.5,
    created_at               TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at               TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_member_verified_neighborhood
        FOREIGN KEY (verified_neighborhood_id) REFERENCES neighborhood(id),
    CONSTRAINT chk_member_role
        CHECK (role IN ('ROLE_USER', 'ROLE_LOCAL_VERIFIED', 'ROLE_ADMIN')),
    CONSTRAINT chk_member_login_type
        CHECK (login_type IN ('LOCAL', 'KAKAO', 'NAVER')),
    CONSTRAINT uk_member_provider_provider_id
        UNIQUE (provider, provider_id)
);

CREATE INDEX idx_member_verified_neighborhood
    ON member (verified_neighborhood_id);

CREATE TRIGGER trg_member_updated_at
BEFORE UPDATE ON member
FOR EACH ROW
EXECUTE FUNCTION set_updated_at();

-- =========================
-- location_verification
-- =========================
CREATE TABLE location_verification (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    member_id       BIGINT NOT NULL,
    neighborhood_id BIGINT NOT NULL,
    latitude        DOUBLE PRECISION NOT NULL,
    longitude       DOUBLE PRECISION NOT NULL,
    verified        BOOLEAN NOT NULL,
    failure_reason  VARCHAR(255),
    verified_at     TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_location_verification_member
        FOREIGN KEY (member_id) REFERENCES member(id) ON DELETE CASCADE,
    CONSTRAINT fk_location_verification_neighborhood
        FOREIGN KEY (neighborhood_id) REFERENCES neighborhood(id) ON DELETE CASCADE
);

CREATE INDEX idx_location_verification_member
    ON location_verification (member_id, verified_at DESC);

CREATE INDEX idx_location_verification_neighborhood
    ON location_verification (neighborhood_id);

-- =========================
-- product_post
-- =========================
CREATE TABLE product_post (
    id                       BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    seller_id                BIGINT NOT NULL,
    neighborhood_id          BIGINT NOT NULL,
    title                    VARCHAR(120) NOT NULL,
    content                  TEXT NOT NULL,
    price                    INTEGER NOT NULL DEFAULT 0,
    giveaway                 BOOLEAN NOT NULL DEFAULT FALSE,
    category                 VARCHAR(30) NOT NULL,
    representative_image_url VARCHAR(500),
    trade_place              VARCHAR(120),
    status                   VARCHAR(30) NOT NULL DEFAULT 'ON_SALE',
    view_count               BIGINT NOT NULL DEFAULT 0,
    created_at               TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at               TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_product_post_seller
        FOREIGN KEY (seller_id) REFERENCES member(id) ON DELETE CASCADE,
    CONSTRAINT fk_product_post_neighborhood
        FOREIGN KEY (neighborhood_id) REFERENCES neighborhood(id),
    CONSTRAINT chk_product_status
        CHECK (status IN ('ON_SALE', 'RESERVED', 'COMPLETED', 'HIDDEN')),
    CONSTRAINT chk_product_category
        CHECK (category IN (
            'DIGITAL', 'APPLIANCE', 'FURNITURE', 'KITCHEN', 'KIDS',
            'FASHION', 'BEAUTY', 'SPORTS', 'HOBBY', 'BOOK',
            'TICKET', 'PET', 'PLANT', 'ETC', 'WANTED'
        )),
    CONSTRAINT chk_product_price
        CHECK (price >= 0)
);

CREATE INDEX idx_product_post_neighborhood_status
    ON product_post (neighborhood_id, status, created_at DESC);

CREATE INDEX idx_product_post_seller
    ON product_post (seller_id, created_at DESC);

CREATE INDEX idx_product_post_title
    ON product_post (title);

CREATE TRIGGER trg_product_post_updated_at
BEFORE UPDATE ON product_post
FOR EACH ROW
EXECUTE FUNCTION set_updated_at();

-- =========================
-- trade
-- =========================
CREATE TABLE trade (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    product_post_id BIGINT NOT NULL,
    seller_id       BIGINT NOT NULL,
    buyer_id        BIGINT NOT NULL,
    status          VARCHAR(30) NOT NULL DEFAULT 'REQUESTED',
    completed_at    TIMESTAMP,
    created_at      TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_trade_product_post
        FOREIGN KEY (product_post_id) REFERENCES product_post(id) ON DELETE CASCADE,
    CONSTRAINT fk_trade_seller
        FOREIGN KEY (seller_id) REFERENCES member(id) ON DELETE CASCADE,
    CONSTRAINT fk_trade_buyer
        FOREIGN KEY (buyer_id) REFERENCES member(id) ON DELETE CASCADE,
    CONSTRAINT chk_trade_status
        CHECK (status IN ('REQUESTED', 'ACCEPTED', 'COMPLETED', 'CANCELLED')),
    CONSTRAINT chk_trade_not_self
        CHECK (seller_id <> buyer_id)
);

CREATE INDEX idx_trade_seller ON trade (seller_id, created_at DESC);
CREATE INDEX idx_trade_buyer ON trade (buyer_id, created_at DESC);
CREATE INDEX idx_trade_product_post ON trade (product_post_id);

-- =========================
-- review
-- =========================
CREATE TABLE review (
    id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    trade_id    BIGINT NOT NULL UNIQUE,
    reviewer_id BIGINT NOT NULL,
    reviewee_id BIGINT NOT NULL,
    rating      INTEGER NOT NULL,
    content     VARCHAR(500) NOT NULL,
    created_at  TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_review_trade
        FOREIGN KEY (trade_id) REFERENCES trade(id) ON DELETE CASCADE,
    CONSTRAINT fk_review_reviewer
        FOREIGN KEY (reviewer_id) REFERENCES member(id) ON DELETE CASCADE,
    CONSTRAINT fk_review_reviewee
        FOREIGN KEY (reviewee_id) REFERENCES member(id) ON DELETE CASCADE,
    CONSTRAINT chk_review_rating
        CHECK (rating BETWEEN 1 AND 5),
    CONSTRAINT chk_review_not_self
        CHECK (reviewer_id <> reviewee_id)
);

CREATE INDEX idx_review_reviewee
    ON review (reviewee_id, created_at DESC);

