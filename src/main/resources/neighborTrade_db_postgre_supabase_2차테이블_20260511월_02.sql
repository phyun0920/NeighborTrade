-- 2차 테이블 생성 확인
SELECT table_name
FROM information_schema.tables
WHERE table_schema = 'public'
  AND table_name IN (
      'product_image',
      'community_post',
      'community_comment',
      'report',
      'chat_room',
      'chat_message',
      'manner_score_history'
  )
ORDER BY table_name;

-- neighborhood PostGIS 컬럼 확인
SELECT
    column_name,
    data_type,
    udt_name,
    is_nullable
FROM information_schema.columns
WHERE table_schema = 'public'
  AND table_name = 'neighborhood'
  AND column_name IN ('boundary', 'center_point')
ORDER BY ordinal_position;

