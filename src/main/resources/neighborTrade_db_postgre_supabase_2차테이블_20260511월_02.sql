-- 목적:
-- - 기존 1차 SQL(neighborTrade_db_postgre_supabase_20260511월.sql)에 없는 2차 기능 테이블을 추가한다.
-- - PostGIS 위치 인증 컬럼을 추가한다.
-- - 2차 기능 샘플 데이터를 INSERT한다.
-- - UPDATE, DELETE 예시를 제공한다.
--
-- 실행 위치:
-- - Supabase SQL Editor
--
-- 실행 전 주의:
-- - 이 스크립트는 기존 1차 테이블(neighborhood, member, product_post, trade, review)이 이미 있다고 가정한다.
-- - 기존 데이터를 삭제하지 않는 CREATE IF NOT EXISTS / ALTER IF NOT EXISTS 중심 스크립트이다.
-- - 샘플 INSERT는 1차 샘플 데이터의 id 값(동네 1, seller 2, buyer 3, 판매글 1 등)을 기준으로 한다.
-- - 운영 DB에서는 INSERT/UPDATE/DELETE 예시 구간을 실행하기 전에 데이터를 확인한다.

CREATE EXTENSION IF NOT EXISTS postgis;

-- ==========================================================
-- 0. updated_at trigger function
-- ==========================================================

CREATE OR REPLACE FUNCTION set_updated_at()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = CURRENT_TIMESTAMP;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

-- ==========================================================
-- 1. PostGIS columns for neighborhood
-- ==========================================================

ALTER TABLE neighborhood
    ADD COLUMN IF NOT EXISTS boundary geometry(MultiPolygon, 4326),
    ADD COLUMN IF NOT EXISTS center_point geography(Point, 4326);

UPDATE neighborhood
SET center_point = ST_SetSRID(ST_MakePoint(center_longitude, center_latitude), 4326)::geography
WHERE center_point IS NULL;

CREATE INDEX IF NOT EXISTS idx_neighborhood_boundary
    ON neighborhood USING GIST (boundary);

CREATE INDEX IF NOT EXISTS idx_neighborhood_center_point
    ON neighborhood USING GIST (center_point);

-- boundary 샘플 입력 예시
-- 실제 서비스에서는 행정동 경계 GeoJSON/공공데이터를 변환해서 입력한다.
-- 아래 예시는 가산동 중심 근처의 임시 사각형 MultiPolygon이다.
UPDATE neighborhood
SET boundary = ST_GeomFromText(
    'MULTIPOLYGON(((126.8600 37.4650, 126.9100 37.4650, 126.9100 37.4900, 126.8600 37.4900, 126.8600 37.4650)))',
    4326
)
WHERE id = 1
  AND boundary IS NULL;

-- ==========================================================
-- 2. product_image: 판매글 이미지
-- ==========================================================

CREATE TABLE IF NOT EXISTS product_image (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    product_post_id BIGINT NOT NULL,
    original_filename VARCHAR(255) NOT NULL,
    stored_filename VARCHAR(255) NOT NULL,
    image_url VARCHAR(500) NOT NULL,
    content_type VARCHAR(100) NOT NULL,
    file_size BIGINT NOT NULL,
    sort_order INTEGER NOT NULL DEFAULT 0,
    representative BOOLEAN NOT NULL DEFAULT FALSE,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_product_image_post
        FOREIGN KEY (product_post_id) REFERENCES product_post(id) ON DELETE CASCADE
);

CREATE INDEX IF NOT EXISTS idx_product_image_post
    ON product_image (product_post_id, sort_order);

-- ==========================================================
-- 3. community_post: 동네생활 게시글
-- ==========================================================

CREATE TABLE IF NOT EXISTS community_post (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    author_id BIGINT NOT NULL,
    neighborhood_id BIGINT NOT NULL,
    title VARCHAR(120) NOT NULL,
    content TEXT NOT NULL,
    status VARCHAR(30) NOT NULL DEFAULT 'VISIBLE',
    view_count BIGINT NOT NULL DEFAULT 0,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_community_post_author
        FOREIGN KEY (author_id) REFERENCES member(id) ON DELETE CASCADE,
    CONSTRAINT fk_community_post_neighborhood
        FOREIGN KEY (neighborhood_id) REFERENCES neighborhood(id),
    CONSTRAINT chk_community_post_status
        CHECK (status IN ('VISIBLE', 'HIDDEN'))
);

CREATE INDEX IF NOT EXISTS idx_community_post_neighborhood_status
    ON community_post (neighborhood_id, status, created_at DESC);

DROP TRIGGER IF EXISTS trg_community_post_updated_at ON community_post;
CREATE TRIGGER trg_community_post_updated_at
BEFORE UPDATE ON community_post
FOR EACH ROW
EXECUTE FUNCTION set_updated_at();

-- ==========================================================
-- 4. community_comment: 동네생활 댓글
-- ==========================================================

CREATE TABLE IF NOT EXISTS community_comment (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    community_post_id BIGINT NOT NULL,
    author_id BIGINT NOT NULL,
    content VARCHAR(500) NOT NULL,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_community_comment_post
        FOREIGN KEY (community_post_id) REFERENCES community_post(id) ON DELETE CASCADE,
    CONSTRAINT fk_community_comment_author
        FOREIGN KEY (author_id) REFERENCES member(id) ON DELETE CASCADE
);

CREATE INDEX IF NOT EXISTS idx_community_comment_post
    ON community_comment (community_post_id, created_at);

-- ==========================================================
-- 5. report: 신고
-- ==========================================================

CREATE TABLE IF NOT EXISTS report (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    reporter_id BIGINT NOT NULL,
    target_type VARCHAR(40) NOT NULL,
    target_id BIGINT NOT NULL,
    reason VARCHAR(100) NOT NULL,
    detail VARCHAR(1000),
    status VARCHAR(30) NOT NULL DEFAULT 'RECEIVED',
    processor_note VARCHAR(1000),
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    processed_at TIMESTAMP,
    CONSTRAINT fk_report_reporter
        FOREIGN KEY (reporter_id) REFERENCES member(id) ON DELETE CASCADE,
    CONSTRAINT chk_report_target_type
        CHECK (target_type IN ('PRODUCT_POST', 'COMMUNITY_POST', 'COMMUNITY_COMMENT', 'CHAT_MESSAGE')),
    CONSTRAINT chk_report_status
        CHECK (status IN ('RECEIVED', 'REVIEWING', 'RESOLVED', 'REJECTED'))
);

CREATE INDEX IF NOT EXISTS idx_report_status_created
    ON report (status, created_at DESC);

CREATE INDEX IF NOT EXISTS idx_report_target
    ON report (target_type, target_id);

-- ==========================================================
-- 6. chat_room: 채팅방
-- ==========================================================

CREATE TABLE IF NOT EXISTS chat_room (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    product_post_id BIGINT NOT NULL,
    seller_id BIGINT NOT NULL,
    buyer_id BIGINT NOT NULL,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_chat_room_product_post
        FOREIGN KEY (product_post_id) REFERENCES product_post(id) ON DELETE CASCADE,
    CONSTRAINT fk_chat_room_seller
        FOREIGN KEY (seller_id) REFERENCES member(id) ON DELETE CASCADE,
    CONSTRAINT fk_chat_room_buyer
        FOREIGN KEY (buyer_id) REFERENCES member(id) ON DELETE CASCADE,
    CONSTRAINT chk_chat_room_not_self
        CHECK (seller_id <> buyer_id),
    CONSTRAINT uk_chat_room_post_buyer
        UNIQUE (product_post_id, buyer_id)
);

CREATE INDEX IF NOT EXISTS idx_chat_room_seller
    ON chat_room (seller_id, updated_at DESC);

CREATE INDEX IF NOT EXISTS idx_chat_room_buyer
    ON chat_room (buyer_id, updated_at DESC);

DROP TRIGGER IF EXISTS trg_chat_room_updated_at ON chat_room;
CREATE TRIGGER trg_chat_room_updated_at
BEFORE UPDATE ON chat_room
FOR EACH ROW
EXECUTE FUNCTION set_updated_at();

-- ==========================================================
-- 7. chat_message: 채팅 메시지
-- ==========================================================

CREATE TABLE IF NOT EXISTS chat_message (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    chat_room_id BIGINT NOT NULL,
    sender_id BIGINT NOT NULL,
    content VARCHAR(1000) NOT NULL,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_chat_message_room
        FOREIGN KEY (chat_room_id) REFERENCES chat_room(id) ON DELETE CASCADE,
    CONSTRAINT fk_chat_message_sender
        FOREIGN KEY (sender_id) REFERENCES member(id) ON DELETE CASCADE
);

CREATE INDEX IF NOT EXISTS idx_chat_message_room
    ON chat_message (chat_room_id, created_at);

-- ==========================================================
-- 8. manner_score_history: 매너 점수 이력
-- ==========================================================

CREATE TABLE IF NOT EXISTS manner_score_history (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    member_id BIGINT NOT NULL,
    review_id BIGINT,
    before_score DOUBLE PRECISION NOT NULL,
    after_score DOUBLE PRECISION NOT NULL,
    reason VARCHAR(255) NOT NULL,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_manner_history_member
        FOREIGN KEY (member_id) REFERENCES member(id) ON DELETE CASCADE,
    CONSTRAINT fk_manner_history_review
        FOREIGN KEY (review_id) REFERENCES review(id) ON DELETE SET NULL
);

CREATE INDEX IF NOT EXISTS idx_manner_history_member
    ON manner_score_history (member_id, created_at DESC);

